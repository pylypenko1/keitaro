<script>
	function showComment() {
		var comName = document.getElementById('comName').value;
		var comEmail = document.getElementById('comEmail').value;
		var comTextarea = document.getElementById('comTextarea').value;
		var comCheckbox = document.getElementById('comCheckbox').value;

		var pattern = /^[a-z0-9_-]+@[a-z0-9-]+\.[a-z]{2,6}$/i;
		var requiredEmail = comEmail.search(pattern) == 0;

		if (
			comName != '' &&
			comEmail != '' &&
			comTextarea != '' &&
			comCheckbox != '' &&
			requiredEmail == true
		) {
			$('.alert-com').hide();
			$('.page-comment').hide();
			$('.com-add').show();
		} else {
			$('.alert-com').show().fadeOut(3000);
		}
	}
</script>

