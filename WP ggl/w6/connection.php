    <script>
      const submenuToggle = document.getElementById('submenu-toggle');
      const submenu = document.getElementById('submenu');

      submenuToggle.addEventListener('click', (event) => {
        event.preventDefault();

        submenuToggle.classList.toggle('active');

        if (submenuToggle.classList.contains('active')) {
          submenu.style.maxHeight = submenu.scrollHeight + 'px';
        } else {
          submenu.style.maxHeight = '0';
        }
      });

      window.addEventListener('click', (event) => {
        if (!submenu.contains(event.target) && event.target !== submenuToggle) {
          submenu.style.maxHeight = '0';
          submenuToggle.classList.remove('active');
        }
      });
    </script>

    <script>
      function openVideoPopup(videoUrl) {
        const overlay = document.createElement('div');
        overlay.classList.add('video-popup-overlay');

        const popup = document.createElement('div');
        popup.classList.add('video-popup');

        const video = document.createElement('video');
        video.src = videoUrl;
        video.controls = true;
        video.autoplay = true;

        popup.appendChild(video);

        const closeButton = document.createElement('button');
        closeButton.classList.add('close-button');
        closeButton.textContent = 'Закрыть';
        closeButton.addEventListener('click', () => {
          overlay.remove();
        });

        popup.appendChild(closeButton);
        overlay.appendChild(popup);

        document.body.appendChild(overlay);
      }
    </script>

    <script>
      var lastScrollY = 0;
      var ticking = false;
      var header = document.querySelector('.header');

      function updateHeader() {
        if (window.scrollY > lastScrollY) {
          header.classList.add('fixed-header');
        } else {
          header.classList.remove('fixed-header');
        }
        lastScrollY = window.scrollY;
        ticking = false;
      }

      window.addEventListener('scroll', function () {
        if (!ticking) {
          window.requestAnimationFrame(updateHeader);
          ticking = true;
        }
      });
    </script>

